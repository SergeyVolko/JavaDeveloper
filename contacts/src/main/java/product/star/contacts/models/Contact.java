package product.star.contacts.models;

import product.star.contacts.dto.ContactDto;

import java.util.Objects;

public class Contact {
    private long id;
    private String name;
    private String surname;
    private String email;

    public Contact(long id, String name, String surname, String email) {
        this.id = id;
        this.name = name;
        this.surname = surname;
        this.email = email;
    }

    public Contact(ContactDto contactDto) {
        this.id = contactDto.getId();
        this.name = contactDto.getName();
        this.surname = contactDto.getSurname();
        this.email = contactDto.getEmail();
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Contact contact = (Contact) o;
        return id == contact.id;
    }

    @Override
    public int hashCode() {
        return Objects.hashCode(id);
    }
}
